    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String method = jComboBox1.getSelectedItem().toString();
        String trains = jComboBox2.getSelectedItem().toString();
        System.out.println(method+"|"+trains);
        String r1="",r2="",r3="",r4="";
        if (method.contains("Bayes")) {
            NaiveBayes NB = new NaiveBayes(D) ;
            String res="";            
            if (trains.contains("Full")) {
                String ss = "full|bayes";
                 r1 = D.printInfo(ss);
                 r2 = NB.doSchemaFullTraining(D);
                 r4 = "Naive Bayes - Full Training\n";
            }
             else {
                String ss = "10fold|bayes";
                 r1 = D.printInfo(ss);
                 r2 = NB.do10crossFold(D);
                 r4 = "Naive Bayes - 10 Cross Fold\n";
            }
            // System.out.println(res);
            r3=NB.printClassResult();
        } else {
            System.out.print("k-NN -> Input k : ");
            String res="";
            int k= Integer.valueOf(jTextField3.getText());
            knn2 K = new knn2(k,D);
            if (method.contains("Bayes")) {
                String ss= "full|knn,"+k;
                 r1 = D.printInfo(ss);
                 r2 = K.schemaFullTraining();
                 r4 = k+"-NN - Full Training\n";
            }
             else {
                String ss = "10fold|knn,"+k;
                 r1 = D.printInfo(ss);
                 r2 = K.schema10Fold(D);
                  r4 = k+"-NN - 10 CrossFold\n";
            }            
            r3=K.printClassResult();
        }
        Result R = new Result(r1,r2,r3,r4,D.dataName);
        R.setVisible(true);
    }//GEN-LAST:event_jButton3ActionPerformed

