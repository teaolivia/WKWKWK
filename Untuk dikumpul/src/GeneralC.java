    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String s = jTextField1.getText();
        D = new Data(s);
        D.makeDataSet();
        D.checkNumericAttr();
        String jt="";
        jt += "Sumber Data :\t"+D.fileName+"\n";
        jt += "Nama Set Data :"+D.dataName+"\n";
        jt += "Jumlah Data :\t"+D.totalData+"\n";
        jt += "Jumlah Atribut :\t"+D.listAttr.size()+"\n";
        jTextArea1.setText(jt);
        int na=D.listAttr.size();
        String list[] =new String[na];
        for (int i=0;i<na;i++) {
            list[i] = D.listAttr.get(i);
        }
        jList1.setListData(list);
        jList1.setSelectedIndex(0);
        String val = jList1.getSelectedValue().toString();
        String text = D.getInfoAttr(val);
        jTextArea2.setText(text);
        
    }//GEN-LAST:event_jButton2ActionPerformed

